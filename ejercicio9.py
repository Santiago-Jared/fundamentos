##9._  Una tienda ofrece un desuento del 15% sobre el total de la compra y un cliente desea saber cuanto debera pagar por su compra.


precio= f"Ingresa el precio del producto: "
precio = input(precio)
des = (precio * 0.15)
destotal = (precio - des)

f"Tu descuento por tu compra fue de {des} pesos, el descuento total es: {destotal}" 
